class Storage {
    constructor(goods) {
        this.items = goods;
    }
    getItems() {
        return this.items;
    }
    addItem(item) {
        this.items.push(item);
    }
    removeItem(item) {
        if (this.items.indexOf(item) === -1) {
            return `–≠–ª–µ–º–µ–Ω—Ç ${item} –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ –º–∞—Å—Å–∏–≤–µ —Ç–æ–≤–∞—Ä–æ–≤.`;
        } else {
            this.items.splice(this.items.indexOf(item), 1);
        }
    }
}

